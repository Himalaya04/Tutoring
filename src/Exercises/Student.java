package Exercises;

public class Student {
    String name;
    String email;
    int GPA;
    int phoneNum;
    int height;
    int weight;

    public Student(String name, String email, int GPA, int phoneNum, int height, int weight){
        this.name = name;
        this.email = email;
        this.GPA = GPA;
        this.phoneNum = phoneNum;
        this.height = height;
        this.weight = weight;
    }
}
